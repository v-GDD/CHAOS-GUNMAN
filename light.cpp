#include "light.h"

//*************************************************************************************************
//*** マクロ定義 ***
//*************************************************************************************************
#define LIGHT_NUM		(3)			// ライトの数

//*************************************************************************************************
//*** グローバル変数 ***
//*************************************************************************************************
D3DLIGHT9 g_aLight[LIGHT_NUM];		// カメラの情報

//================================================================================================================
// --- ライトの初期化 ---
//================================================================================================================
void InitLight(void)
{
	/*** デバイスの取得 ***/
	LPDIRECT3DDEVICE9 pDevice = GetDevice();
	D3DXVECTOR3 vecDir[3];			// ライトの方向ベクトル

	/***ライトの情報を初期化 ***/
	ZeroMemory(&g_aLight[0], sizeof(D3DLIGHT9) * 3);

	/*** ライトの種類を設定 ***/
	g_aLight[0].Type = D3DLIGHT_DIRECTIONAL;
	g_aLight[1].Type = D3DLIGHT_DIRECTIONAL;
	g_aLight[2].Type = D3DLIGHT_DIRECTIONAL;

	/*** ライトの拡散光を設定 ***/
	g_aLight[0].Diffuse = D3DXCOLOR(1.0f, 1.0f, 1.0f, 1.0f);
	g_aLight[1].Diffuse = D3DXCOLOR(0.7f, 0.7f, 0.7f, 1.0f);
	g_aLight[2].Diffuse = D3DXCOLOR(0.3f, 0.3f, 0.3f, 1.0f);

	/*** ライトの方向を設定 ***/
	vecDir[0] = D3DXVECTOR3(0.2f, -0.8f, -0.4f);
	/** ベクトルの正規化 **/
	D3DXVec3Normalize(&vecDir[0], &vecDir[0]);
	g_aLight[0].Direction = vecDir[0];

	vecDir[1] = D3DXVECTOR3(-0.3f, 0.7f, 0.5f);
	/** ベクトルの正規化 **/
	D3DXVec3Normalize(&vecDir[1], &vecDir[1]);
	g_aLight[1].Direction = vecDir[1];

	vecDir[2] = D3DXVECTOR3(0.0f, -0.5f, 0.5f);
	/** ベクトルの正規化 **/
	D3DXVec3Normalize(&vecDir[2], &vecDir[2]);
	g_aLight[2].Direction = vecDir[2];

	/*** ライトを設定する ***/
	pDevice->SetLight(0, &g_aLight[0]);
	pDevice->SetLight(1, &g_aLight[1]);
	pDevice->SetLight(2, &g_aLight[2]);

	/*** ライトを有効にする ***/
	pDevice->LightEnable(0, TRUE);
	pDevice->LightEnable(1, TRUE);
	pDevice->LightEnable(2, TRUE);

	EndDevice();
}

